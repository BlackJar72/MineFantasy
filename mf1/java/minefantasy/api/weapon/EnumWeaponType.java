package minefantasy.api.weapon;

public enum EnumWeaponType {
	/**
	 * Defines a small blade smaller than vanilla(Dagger, Dirk, etc)
	 */
	SMLBLADE,
	/**
	 * Defines a blade as long as a vanilla sword (Vanilla Sword, Shortsword, etc)
	 */
	MEDBLADE,
	/**
	 * Defines a longer than natural blade(Longsword, Broadsword, etc)
	 */
	LONGBLADE,
	/**
	 * Defines a really long blade(Bastard Sword, Claymore, etc) 
	 */
	BIGBLADE,
	
	/**
	 * Defines a small blunt weapon(Mace, Warpick, etc)
	 */
	SMLBLUNT,
	/**
	 * Defines a large blunt weapon(Morningstar, Warhammer, etc)
	 */
	BIGBLUNT,
	
	/**
	 * Defines a small axe(Tommohawk, Axe Tool, etc)
	 */
	SMLAXE,
	/**
	 * Defines a mid sized axe(Waraxe, etc)
	 */
	AXE,
	/**
	 * Defines a large axe(Battleaxe, Bearded Axe, etc)
	 */
	BIGAXE,
	
	/**
	 * Defines a small polearm(Primitive Spear, Spear, etc);
	 */
	POLEARM,
	/**
	 * Defines a larger blade polearm(Halbeard, etc)
	 */
	BIGPOLEARM,
	/**
	 * Defines a light polearm(Quaterstaff, etc)
	 */
	STAFF;
}
